#Suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5

#multiplicacion y division (* y /)
multiplicacion = 12 * 5
division = 12 / 5 #Nos devuelve un dato float

#Potenciacion (Exponent)(**)
exponente = 12**5

#Division baja (//)
division_baja = 12//5 #Devuelve entero hacia abajo

#Resto o Modulo (%)
resto = 12 % 5

#type() nos devuelve el tipo de dato que es
tipo_dato = type(5)
print(tipo_dato)


print(suma)
print(resta)
print(multiplicacion)
print(division)
print(exponente)
print(division_baja)
print(resto)
