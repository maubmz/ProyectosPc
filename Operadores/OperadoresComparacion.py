es_distinto_que = (5 != 6)
print(f"5 es distinto que 6: {es_distinto_que}")

mayor_que = (5 > 6)
print(f"5 es mayor que 6: {mayor_que}")

menor_que = (5 < 6)
print(f"5 es menor que 6: {menor_que}")

mayor_o_igual = (5 <= 6)
print(f"5 es mayor o igual que 6: {mayor_o_igual}")

menor_o_igual = (5 >= 6)
print(f"5 es menor o igual que 6: {menor_o_igual}")

